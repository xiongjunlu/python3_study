############################################ 特殊方法 ############################################

# __init__(self, ...) 返回新创建对象，准备使用时被调用
# __del__(self) 对象被删除前调用
# __str__(self) 使用print函数或str()方法时调用
# __lt__(self, other) 使用（<）运算符时调用，其他运算符有各自特殊方法被调用
# __getitem__(self, key) 使用x[key]索引操作时调用
# __len__(self) 针对序列对象使用len()方法时调用

